// Diferencia entre Funciones y Métodos

const numero1 = 20;
const numero2 = "20";


console.log( parseInt(numero2) ); // Esto es una función

console.log( numero1.toString()); // Esto es un método


// Terminan siendo practicamente lo mismo
// La forma en que son nombrado (método o función) tiene que ver con el contexto en el que son utilizadas



